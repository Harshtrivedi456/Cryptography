# Round constants
Rcon = [0x00,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1B,0x36]
# State and RoundKey
state = [[0]*4 for _ in range(4)]
RoundKey = [0]*176
def print_state(label):
 print(label)
 for r in range(4):
    print(" ".join(f"{state[r][c]:02X}" for c in range(4)))
    print()
# SubBytes
def SubBytes():
 for r in range(4):
    for c in range(4):
        state[r][c] = sbox[state[r][c]]
    # ShiftRows
def ShiftRows():
 state[1][0],state[1][1],state[1][2],state[1][3] = \
 state[1][1],state[1][2],state[1][3],state[1][0]
 state[2][0],state[2][1],state[2][2],state[2][3] = \
 state[2][2],state[2][3],state[2][0],state[2][1]
 state[3][0],state[3][1],state[3][2],state[3][3] = \
 state[3][3],state[3][0],state[3][1],state[3][2]
# Multiply by 2 in GF(2^8)
def xtime(x):
 return ((x << 1) & 0xFF) ^ (0x1B if x & 0x80 else 0x00)
# MixColumns
def MixColumns():
 for c in range(4):
    a0,a1,a2,a3 = state[0][c],state[1][c],state[2][c],state[3][c]
    r0 = xtime(a0) ^ (xtime(a1) ^ a1) ^ a2 ^ a3
    r1 = a0 ^ xtime(a1) ^ (xtime(a2) ^ a2) ^ a3
    r2 = a0 ^ a1 ^ xtime(a2) ^ (xtime(a3) ^ a3)
    r3 = (xtime(a0) ^ a0) ^ a1 ^ a2 ^ xtime(a3)
    state[0][c],state[1][c],state[2][c],state[3][c] = r0,r1,r2,r3
# AddRoundKey
def AddRoundKey(round):
 for c in range(4):
    for r in range(4):
        state[r][c] ^= RoundKey[round*16 + c*4 + r]
# KeyExpansion
def KeyExpansion(key):
 for i in range(Nk*4):
     RoundKey[i] = key[i]
 for i in range(Nk, Nb*(Nr+1)):
    temp = RoundKey[(i-1)*4:(i-1)*4+4]
 if i % Nk == 0:
 # RotWord
    temp = temp[1:] + temp[:1]
 # SubWord
 temp = [sbox[b] for b in temp]
 temp[0] ^= Rcon[i//Nk]
 for j in range(4):
    RoundKey[i*4+j] = RoundKey[(i-Nk)*4+j] ^ temp[j]
# AES Encrypt
def AES_encrypt(input_bytes, key):
 # Copy input to state
 for r in range(4):
    for c in range(4):
        state[r][c] = input_bytes[r + 4*c]
        KeyExpansion(key)
        print_state("Initial State:")
        AddRoundKey(0)
        print_state("After AddRoundKey(0):")
        for round in range(1, Nr):
            SubBytes(); print_state("After SubBytes:")
            ShiftRows(); print_state("After ShiftRows:")
            MixColumns(); print_state("After MixColumns:")
            AddRoundKey(round); print_state(f"After AddRoundKey (Round {round}):")
            print(f"---- End of Round {round} ----\n")
            SubBytes()
            ShiftRows()
            AddRoundKey(Nr)
            print_state("Final State (Ciphertext):")
